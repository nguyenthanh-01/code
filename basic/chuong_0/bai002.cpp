#include <bits/stdc++.h>
using namespace std;

typedef long long ll;

int main() {
    int a1; cin >> a1;
    ll a2; cin >> a2;
    char a3; cin >> a3;
    float a4; cin >> a4;
    double a5; cin >> a5;
    cout << a1 << endl;
    cout << a2 << endl;
    cout << a3 << endl;
    printf("%.2f \n", a4);
    printf("%.9lf \n", a5);
    return 0;
}
